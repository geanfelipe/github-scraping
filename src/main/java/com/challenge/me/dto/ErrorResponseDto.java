
package com.challenge.me.dto;

import java.io.Serializable;

public class ErrorResponseDto implements Serializable {

	private static final long serialVersionUID = -3091594584842916959L;

	private int status;
	private String message;

	public ErrorResponseDto() {}

	public ErrorResponseDto(final int status, final String message) {
		super();
		this.status = status;
		this.message = message;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(final int status) {
		this.status = status;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(final String message) {
		this.message = message;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + (message == null ? 0 : message.hashCode());
		result = prime * result + status;
		return result;
	}

	@Override
	public boolean equals(final Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		final ErrorResponseDto other = (ErrorResponseDto) obj;
		if (message == null) {
			if (other.message != null) {
				return false;
			}
		} else if (!message.equals(other.message)) {
			return false;
		}
		if (status != other.status) {
			return false;
		}
		return true;
	}

}
